N_duplicates = R['Nall'] - N_unique

cif_refine_special_details += f"""
Refinement statistics relevant for the non-linear least-squares minimisation of wR(all):
Number of reflections in refinement (obs/all):   {R['Nobs']} / {R['Nall']}
Number of reflections present more than once:    {N_duplicates}
Number of reflections unique in point group 1:   {N_unique}
Robs:   {R['Robs']:6.4f}
Rall:   {R['Rall']:6.4f}
wRall:  {R['wRall']:6.4f}
"""

