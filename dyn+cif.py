cif_refine_special_details = ''';
Calculated intensities based on dynamical theory of electron diffraction.
'''
m83_blocks = set(m83df['Block'])

# Statistics and thickness from individual data sets
cif_refine_special_details += f"Number of individual data blocks in refinement: {len(m83_blocks)}\n"
cif_refine_special_details += "Block     Thickness     Nobs    Nall    Robs    Rall   wRall\n"

for BlockID in m83_blocks:
    thicknesses = set(m42_frames['thickness'][ m42_frames['BlockID'] == BlockID ])
    if len(thicknesses) == 1:
        thickness = f'{thicknesses.pop():>13.2f}'
    else:
        tmin, tmax = min(thicknesses), max(thicknesses)
        thickness = f'{tmin:.0f}<t<{tmax:.0f}'
        thickness = f'{thickness:>13}'

    
    R = calc_R_factors( m83df[ m83df['Block'] == BlockID] )
    cif_refine_special_details += f"{BlockID:5} {thickness} {R['Nobs']:>8}{R['Nall']:>8d}{R['Robs']:>8.4f}{R['Rall']:>8.4f}{R['wRall']:>8.4f}\n"
cif_refine_special_details += "Thickness given in Angstrom.\n"
if 'thickmodel' not in m42_settings['commands']:
    cif_refine_special_details += "A block-shaped crystal with constant thickness is assumed.\n"
else:
    data = m42_settings['commands'].split()
    tmp = data.index('thickmodel')
    cif_refine_special_details += f"A crystal with a thickness distribution of a '{data[tmp+1]}' is assumed.\n"
    cif_refine_special_details += "The refined thickness corresponds to the maximum thickness of the shape.\n"

# angle-dependent thickness correction 
